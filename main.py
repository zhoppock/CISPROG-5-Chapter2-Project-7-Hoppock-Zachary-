# We are going to calculate how many minutes occur in a year:

# Initialize the following variables:
# Days of a year = 365
# Hours in a day = 24
# Minutes in an hour = 60
daysOfYear = 365
hoursOfDay = 24
minutesOfHour = 60

# Compute the number of minutes in a year using the formula:
# Minutes in a year = Days of a year * Hours in a day * Minutes in an hour
minutesOfYear = daysOfYear * hoursOfDay * minutesOfHour

#Print the minutes in a year
print("There are " + str(minutesOfYear) + " minutes in a year.")